# =====================================================================
# CONFIG — единый источник правды для параметров проекта
# ---------------------------------------------------------------------
# Раньше symbol/interval/window/комиссии/даты были захардкожены в ~24
# местах в 5 файлах. Это давало риск «тихого» рассинхрона: например,
# обучение на window=30, а live-торговля на window=50 — те же деревья
# получают разные входы и ведут себя непредсказуемо, без ошибки.
# Теперь всё в одном месте: меняешь здесь — меняется везде.
# =====================================================================

from dataclasses import dataclass, field
from typing import List, Tuple


@dataclass(frozen=True)
class Config:
    # --- Рынок ---
    symbol: str = "BTCUSDT"
    interval: str = "1h"
    window: int = 50               # окно индикаторов (SMA/EMA/LWMA и пр.)

    # --- Торговые издержки и сайзинг ---
    commission: float = 0.001          # 0.1% за сделку
    short_cost_per_bar: float = 0.001  # funding-rate за бар удержания шорта
    risk_percent: float = 0.02         # доля баланса на позицию
    initial_balance: float = 10000.0   # стартовый баланс для бэктеста/симуляции

    # --- Пороги решающего правила (мета-сигнал) ---
    long_threshold: float = 0.5        # meta > long_threshold  -> LONG
    short_threshold: float = -0.5      # meta < short_threshold -> SHORT

    # --- Периоды обучения (смесь режимов рынка) ---
    # Список (start, end) в формате dd-mm-YYYY для load_period().
    train_periods: List[Tuple[str, str]] = field(default_factory=lambda: [
        ("01-01-2024", "02-01-2024"),  # январь 2024 — восходящий тренд
        ("09-01-2023", "10-01-2023"),  # сентябрь 2023 — боковой рынок
    ])

    # --- Периоды out-of-sample валидации ---
    val_bull: Tuple[str, str] = ("02-01-2024", "03-01-2024")  # бычий (февраль 2024)
    val_bear: Tuple[str, str] = ("05-01-2023", "06-01-2023")  # боковой/медвежий (май 2023)


# Глобальный экземпляр — импортируется во всех модулях.
CFG = Config()


# =====================================================================
# V2 — «честная эволюция» с защитой от переобучения
# ---------------------------------------------------------------------
# Отличия от v1:
#  • мульти-актив: стратегия обязана работать на нескольких монетах сразу
#  • walk-forward: fitness = средняя по окнам − штраф за разброс
#  • parsimony: штраф за размер дерева (простое обобщается лучше)
#  • отбор по валидации (последние окна), финальный HOLDOUT — нетронутые
#    последние holdout_days, используется ОДИН раз в конце
# =====================================================================

@dataclass(frozen=True)
class V2Config:
    # --- Данные ---
    assets: Tuple[str, ...] = ("BTCUSDT", "ETHUSDT", "SOLUSDT",
                               "BNBUSDT", "XRPUSDT")
    interval: str = "1h"
    history_days: int = 540        # глубина истории на обучение+валидацию
    holdout_days: int = 45         # нетронутый финальный тест (последние дни)
    n_train_windows: int = 6       # окон обучения на актив (равномерно)
    n_val_windows: int = 2         # окон валидации на актив (самые свежие)
    window_bars: int = 600         # размер одного окна в барах

    # --- Анти-переобучение ---
    alpha_stability: float = 0.5   # штраф за разброс прибыли между окнами
    lambda_size: float = 0.15      # штраф за размер дерева (за узел сверх лимита)
    size_free: int = 10            # «бесплатных» узлов дерева
    # Нормализация окон: цены делятся на первую цену окна, чтобы дерево
    # НЕ могло «опознать актив» по абсолютной цене (BTC~60000 vs XRP~0.5)
    # и строить asset-specific ветвления — это обходило бы мульти-активную
    # защиту. PnL инвариантен к нормализации ((a-b)/b не зависит от масштаба).
    normalize: bool = True

    # --- Эволюция v2 (умеренные: eval в ~12 раз дороже v1) ---
    pop_size: int = 120
    offspring: int = 150
    ngen: int = 40

    cache_file: str = "v2_data_cache.pkl"


CFG2 = V2Config()
